export function getCategories(): Category[] {
	return ['Plant-Based', 'Cultivated Meat', 'Fermentation', 'Hybrid', 'Ingredients'];
}

export function getFundingStages(): FundingStage[] {
	return ['Seed', 'Series A', 'Series B', 'Series C', 'Series D+', 'IPO', 'Acquired'];
}

export function getStatuses(): Status[] {
	return ['Active', 'Acquired', 'IPO', 'Shutdown'];
}

export function getTechnologies(): string[] {
	const techs = new Set<string>();
	altProteinCompanies.forEach(c => techs.add(c.technology));
	return Array.from(techs).sort();
}

export function getCountries(): string[] {
	const countries = new Set<string>();
	altProteinCompanies.forEach(c => countries.add(c.country));
	return Array.from(countries).sort();
}

export function getCompanyById(id: string): AltProteinCompany | undefined {
	return altProteinCompanies.find(c => c.id === id);
}

export function getCompaniesByCategory(category: Category): AltProteinCompany[] {
	return altProteinCompanies.filter(c => c.category === category);
}

export function getCompaniesByFundingStage(stage: FundingStage): AltProteinCompany[] {
	return altProteinCompanies.filter(c => c.fundingStage === stage);
}
